l= float(input("Enter length of rectangle: "))
b= float(input("Enter breadth of rectangle: "))
r= float(input("Enter radius of circle: "))
ar= l*b
pr= 2*(l+b)
ac= 3.14*r*r
cc= 2*3.14*r
print("\nThe area of rectangle is %0.2f" %ar)
print("The perimeter of rectangle is %0.2f" %pr)
print("\nThe area of circle is %0.2f" %ac)
print("The circumference of circle is %0.2f" %cc)

