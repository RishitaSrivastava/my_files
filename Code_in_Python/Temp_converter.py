#4. Temperature of a city in Fahrenheit degrees is input through the keyboard. Write a program to convert this temperature into Centigrade degrees.

f= float(input("Enter Temperature of a City in Fahrenheit Degrees: "))
c= (f-32)*5/9
print("The Temperature in Centigrade Degree: %0.2f" %c)

#Output:-
#Enter Temperature of a City in Fahrenheit Degrees: 89
#The Temperature in Centigrade Degree: 31.67